    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        try {
            String reply = JOptionPane.showInputDialog(null, "Enter ID", "Input ID", JOptionPane.INFORMATION_MESSAGE);

            if (reply != null){
                String file;
                
                //differentiate whether user is student or staff
                if(reply.matches("TP(.*)")){
                    file = "Student.txt";
                } else {
                    file = "Staff.txt";
                } 
                
                ReadFile rf = new ReadFile();
                
                if (rf.isValid(file, reply) == true){
                    this.dispose();
                    Return frmReturn = new Return(reply, 1);
                    frmReturn.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Incorrect ID. Please try again.", "Invalid ID", JOptionPane.ERROR_MESSAGE);
                }}
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "The file does not exist. Please enter a valid file name.", "File Not Found", JOptionPane.ERROR_MESSAGE);   
            }
    }//GEN-LAST:event_btnReturnActionPerformed

