    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
    try{
        if (txtID.getText().isEmpty() || txtPassword.getPassword().length == 0){ //https://stackoverflow.com/questions/13460582/how-to-check-if-jpassword-field-is-null
            JOptionPane.showMessageDialog(null, "Please enter ID or password!", "Missing Input", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
        } else{
        String id = txtID.getText();
        String password = (String.valueOf(txtPassword.getPassword()));
        String input = id + "/" + password;
        
        String file;
        int isValid = 0;

            if(id.matches("TP(.*)")){
                file = "Student.txt";
            } else {
                file = "Staff.txt";
            }                

            ReadFile rf = new ReadFile();
            
            // if input is valid, check if the user is admin
            if (rf.login(file, input) == true){
                if (id.equals("admin")){
                    isValid = 2;
                } else {
                    isValid = 1;
                }
            }

            switch (isValid) {
                case 1:  //student or staff
                    JOptionPane.showMessageDialog(null, "Login successful", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    Homepage homepage = new Homepage(id);
                    homepage.setVisible(true);
                    break;
                case 2:  //admin
                    JOptionPane.showMessageDialog(null, "Login successful", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    AdminPage ap = new AdminPage();
                    ap.setVisible(true);
                    break;
                default:  
                    JOptionPane.showMessageDialog(null, "Invalid ID or password. Please try again.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                    txtID.requestFocus();
                    txtID.selectAll();
                    break;
            }
        }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "The file does not exist. Please enter a valid file name.", "File Not Found", JOptionPane.ERROR_MESSAGE);        
        }
    }//GEN-LAST:event_btnLoginActionPerformed
    
