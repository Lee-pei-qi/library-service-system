     private String UpdateRow(long BookID, String textline) throws FileNotFoundException{    
        
           File f = new File(file);
           Scanner sc = new Scanner(f);
           String line = "";
           StringBuilder sb = new StringBuilder();
           
           while(sc.hasNextLine()){
               line = sc.nextLine();
               String[] data = line.split("/");

               if (Long.parseLong(data[0]) == BookID){
                   sb.append(textline);
                   sb.append("\n");
               } else {
               sb.append(line);
               sb.append("\n");
               }
           }
           sc.close();
           return sb.toString();
    }
     
