    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        
        if (txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a Book ID!", "Missing Input", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
        } else {
        try{
        // check the format of ID input
        BookID = Long.parseLong(txtID.getText());
        
        String file = "Book.txt";
        
        ReadFile rf = new ReadFile();
        
        // display msgbox for invalid Book ID  
        if (rf.hasBook(file, BookID) == false){
               JOptionPane.showMessageDialog(null, "Invalid ID! Please try again.", "Invalid ID", JOptionPane.ERROR_MESSAGE);
               txtID.requestFocus();
               txtID.selectAll();
        } else {
        //get the book details from txtfile
        String line = rf.getLine();
        String[] data = line.split("/");
        
        txtTitle.setText(data[1]);
        txtAuthor.setText(data[2]);
        }        
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "The file does not exist. Please enter a valid file name.", "File Not Found", JOptionPane.ERROR_MESSAGE);        
        }
            catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a valid Book ID!", "Invalid Book ID", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
            txtID.selectAll();
        }}
    }//GEN-LAST:event_btnEnterActionPerformed

    
    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        if (txtID.getText().isEmpty() || txtTitle.getText().isEmpty() || txtDate.getDate().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill in the details!", "Missing Input", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
        } else {
            try {
            BookID = Long.parseLong(txtID.getText());
            String file = "BookBorrow.txt";
            
            ReadFile rf = new ReadFile();
            
            //check if the book is available to borrow
            if (rf.hasBook(file,BookID) == false){
            String title = txtTitle.getText();
            String BorrowerID = txtBorrowerID.getText();
            
            // https://www.youtube.com/watch?v=4_pvTD5qx0Y
            String date = ((JTextField)txtDate.getDateEditor().getUiComponent()).getText();
            
            BookBorrow book = new BookBorrow(BookID, title, date, BorrowerID);
            
            // check no of books user is borrowing - max 3 books
            if (rf.hasUser(file, UserID) && rf.getBorrowTimes() >= 3){
                JOptionPane.showMessageDialog(null, "You have reached the maximum number of books borrowed! \n\n Please return a book first!", "Maximum Book Borrowed", JOptionPane.ERROR_MESSAGE);
            } 
            else {
            // calculate due date
            book.setDueDate(date);
            
            //write to bookBorrow.txt
            WriteFile data = new WriteFile(file, true);        
            data.writeToFile(book.toString());
            
            //write to Transaction.txt
            Transaction trans = new Transaction(BookID, title, date, BorrowerID);
            WriteFile wf = new WriteFile("Transaction.txt", true);        
            wf.writeToFile(trans.toString());
            
            JOptionPane.showMessageDialog(null, "Borrow successful! \n\n Please return BEFORE " + book.getDueDate(), "Borrow Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            
            if (isAdmin == 0){
            Homepage homepage = new Homepage(UserID);
            homepage.setVisible(true);
            } else {
                AdminPage ap = new AdminPage();
                ap.setVisible(true);
            }
            }
            } else{
               JOptionPane.showMessageDialog(null, "The book has been borrowed. \n Please enter another valid Book ID!", "Invalid Book", JOptionPane.ERROR_MESSAGE);
               txtID.requestFocus();
               txtID.selectAll();
            }
            
        } catch (ParseException ex){
            JOptionPane.showMessageDialog(null, "Invalid Book ID.", "Invalid ID", JOptionPane.ERROR_MESSAGE); 
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "The file does not exist. Please enter a valid file name.", "File Not Found", JOptionPane.ERROR_MESSAGE); 
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "ERROR! Record is not written to file.", "Record Not Written", JOptionPane.ERROR_MESSAGE); 
        }}
        
         }//GEN-LAST:event_btnConfirmActionPerformed

