    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a Book ID!", "Missing Input", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
        } else {
        try {
            ID = Long.parseLong(txtID.getText());
            
            ReadFile rf = new ReadFile();
            
            //check if the book is borrowed to display book status
            if (rf.hasBook("BookBorrow.txt", ID) == true){
                status = "Borrowed";
            } else {
                status = "Available";
            }
            
            // check if bookID is valid
            if (rf.hasBook(file, ID) == true){
            String line = rf.getLine();
            String[] data = line.split("/");

            DefaultTableModel model = (DefaultTableModel)tblSearch.getModel();
            model.addRow(new Object[]{data[0], data[1], data[2], data[3], data[4], data[5], data[6], status});
            
            //show table and button if book is retrieved successfully
            panelTable.setVisible(true);
            btnEdit.setVisible(true);
            btnDelete.setVisible(true);
            } else {
               JOptionPane.showMessageDialog(null, "Invalid ID! Please try again.", "Invalid ID", JOptionPane.ERROR_MESSAGE);
               txtID.requestFocus();
               txtID.selectAll();
           }
        }
         catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a valid Book ID!", "Invalid Book ID", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
            txtID.selectAll();
         } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "The file does not exist. Please enter a valid file name.", "File Not Found", JOptionPane.ERROR_MESSAGE); 
        }}
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         DefaultTableModel model = (DefaultTableModel)tblSearch.getModel();
         
         //check if a table row is selected
         if (tblSearch.getSelectionModel().isSelectionEmpty()){
                JOptionPane.showMessageDialog(null, "Please select a row!", "Missing Input", JOptionPane.ERROR_MESSAGE);
         } else {
             int reply = JOptionPane.showConfirmDialog(null, "Do you want to delete the book?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
        
            if(reply == JOptionPane.YES_OPTION){
            try {
                int selectedRow = tblSearch.getSelectedRow();
                
                ID = Long.parseLong((String)model.getValueAt(selectedRow, 0));
                status = (String)model.getValueAt(selectedRow, 7);
                
                // if book is not borrowed, can be deleted
                if (status.equals("Available")){
                    Book book = new Book(ID, (String)model.getValueAt(selectedRow, 1), (String)model.getValueAt(selectedRow, 2), (String)model.getValueAt(selectedRow, 3), Double.parseDouble((String)model.getValueAt(selectedRow, 4)), Integer.parseInt((String)model.getValueAt(selectedRow, 5)), (String)model.getValueAt(selectedRow, 6));

                    WriteFile wf = new WriteFile(file);
                    wf.deleteRow(book.toString());
                    JOptionPane.showMessageDialog(null, "Delete successful!", "Delete Success", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    Search search = new Search();
                    search.setVisible(true);
                } else {
                     JOptionPane.showMessageDialog(null, "The book is borrowed! Book cannot be deleted!", "Delete Failed", JOptionPane.ERROR_MESSAGE);
                }
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "ERROR! Record is not written to file.", "Record Not Written", JOptionPane.ERROR_MESSAGE);    
            }
        }}
    }//GEN-LAST:event_btnDeleteActionPerformed

