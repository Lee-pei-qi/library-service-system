    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        if (txtID.getText().isEmpty() || txtTitle.getText().isEmpty() || txtAuthor.getText().isEmpty() || txtGenre.getText().isEmpty() || txtPrice.getText().isEmpty() || txtYear.getText().isEmpty() || txtPublisher.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill in the details!", "Missing Input", JOptionPane.ERROR_MESSAGE);
        } else {
        try{
            long ID = Long.parseLong(txtID.getText());
            String title = txtTitle.getText();
            String author = txtAuthor.getText();
            String genre = txtGenre.getText();
            double price = Double.parseDouble(txtPrice.getText());
            int year = Integer.parseInt(txtYear.getText());
            String publisher = txtPublisher.getText();

            Book book = new Book(ID, title, author, genre, price, year, publisher);
            
            String file = "Book.txt";
            
            ReadFile rf = new ReadFile();
            
            //check if the Book ID has been recorded
            if (rf.hasBook(file, ID) == true){
                JOptionPane.showMessageDialog(null, "The same Book ID has been used!", "Duplication of ID", JOptionPane.ERROR_MESSAGE);
                txtID.requestFocus();
                txtID.selectAll();
            } else {   
            //insert new book record
            WriteFile data = new WriteFile(file, true);
            data.writeToFile(book.toString());
            JOptionPane.showMessageDialog(null, "Insert successful!", "Insert Success", JOptionPane.INFORMATION_MESSAGE);
            clear();
            }
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter input with correct format!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
            txtID.selectAll();
        } catch (IOException e){
            JOptionPane.showMessageDialog(null, "ERROR! Record is not written to file.", "Record Not Written", JOptionPane.ERROR_MESSAGE);
        }}
    }//GEN-LAST:event_btnInsertActionPerformed
    
