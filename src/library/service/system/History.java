    public History(String id, int isAdmin) throws FileNotFoundException {
        initComponents();
        userID = id;
        this.isAdmin = isAdmin;
        
        //set icon
        ImageIcon icon = new ImageIcon("apu.jpg"); 
        setIconImage(icon.getImage());
        
        //display record in table
        DefaultTableModel model = (DefaultTableModel)tblHistory.getModel();
        ReadFile rf = new ReadFile();
        
        //display borrow history of user
        if (rf.hasUser("Transaction.txt", userID) == true){
           String line = rf.getLine();               //get sb.tostring() - all borrow records
           String[] record = line.split("\n");       //split and separate each bookborrow record row
           
           for (int i=0; i<record.length; i++){
                String[] data = record[i].split("/");   //split details of each book
                model.addRow(new Object[]{data[3], data[0], data[1], data[4]});  // https://www.youtube.com/watch?v=F0Zq2fAUpXg&t=243s
           }     
        }
    }

